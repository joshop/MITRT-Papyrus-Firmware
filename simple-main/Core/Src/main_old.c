int can_target;
void Process_UART_Command(char *cmd)
{
  if (cmd[0] == '1') {
    can_target = 0x123;
  } else if (cmd[0] == '2') {
    can_target = 0x124;
  } else {
    UART2_Print("Invalid target\r\n");
    return;
  }
  cmd += 2;
  if (!strcmp(cmd, "r1 on"))       CAN_Send(0x01);
  else if (!strcmp(cmd, "r1 off")) CAN_Send(0x02);
  else if (!strcmp(cmd, "r2 on"))  CAN_Send(0x03);
  else if (!strcmp(cmd, "r2 off")) CAN_Send(0x04);
  else if (!strcmp(cmd, "r3 on"))  CAN_Send(0x05);
  else if (!strcmp(cmd, "r3 off")) CAN_Send(0x06);
  else if (!strcmp(cmd, "get r1")) CAN_Send(0x10);
  else if (!strcmp(cmd, "get r2")) CAN_Send(0x11);
  else if (!strcmp(cmd, "get r3")) CAN_Send(0x12);
  else if (!strcmp(cmd, "get all"))CAN_Send(0x13);
  else if (!strcmp(cmd, "reset"))  CAN_Send(0xFF);
  else UART2_Print("Unknown command\r\n");
}
void CAN_Send(uint8_t cmd)
{
  FDCAN_TxHeaderTypeDef txHeader = {0};
  FDCAN_RxHeaderTypeDef rxHeader;
  uint8_t txData[8] = {cmd};
  uint8_t rxData[8];

  txHeader.Identifier = can_target;
  txHeader.IdType = FDCAN_STANDARD_ID;
  txHeader.TxFrameType = FDCAN_DATA_FRAME;
  txHeader.DataLength = FDCAN_DLC_BYTES_1;
  txHeader.FDFormat = FDCAN_CLASSIC_CAN;
  txHeader.BitRateSwitch = FDCAN_BRS_OFF;

  HAL_FDCAN_AddMessageToTxFifoQ(&hfdcan1, &txHeader, txData);

  /* Wait for response */

  uint32_t tick = HAL_GetTick();

  while (HAL_FDCAN_GetRxFifoFillLevel(&hfdcan1, FDCAN_RX_FIFO0) == 0 && HAL_GetTick() - tick < 500);

  if (HAL_FDCAN_GetRxFifoFillLevel(&hfdcan1, FDCAN_RX_FIFO0) == 0) {
    UART2_Print("(NO ACK)\r\n");
    return;
  }

  HAL_FDCAN_GetRxMessage(&hfdcan1, FDCAN_RX_FIFO0, &rxHeader, rxData);

  uint16_t resp_id = rxHeader.Identifier;

  /* Decode response */
  char out[64];

  if (cmd >= 0x10 && cmd <= 0x12)
  {
    sprintf(out, "(%x) R%d=%d\r\n", resp_id, cmd - 0x0F, rxData[1]);
  }
  else if (cmd == 0x13)
  {
    sprintf(out, "(%x) R1=%d R2=%d R3=%d\r\n", resp_id,
            rxData[1], rxData[2], rxData[3]);
  }
  else
  {
    sprintf(out, "(%x) OK\r\n", resp_id);
  }

  UART2_Print(out);
}
